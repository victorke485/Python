# Built-in Math Functions
print(max(4, 77, 453))
print(min(4, 77, 453))

# abs() - returns the absolute value of the specified number
print(abs(-43.3435))

# pow(x, y) - returns the value of x to power of y
print(pow(5, 4))

# The math module
import math

print(math.sqrt(625)) # returns square root of a number

print(math.ceil(1.4)) # rounds a number upwards to its nearest integer
print(math.floor(1.4)) # rounds a number downwards to its nearest integer

print(math.pi) # returns the value of pi