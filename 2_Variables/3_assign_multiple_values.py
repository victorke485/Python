# Many values to multiple variables
x, y, z = "Arsenal", "Liverpool", "Chelsea"
print(x)
print(y)
print(z)

# One value to multiple variables
a = b = c = "Victor"
print(a)
print(b)
print(c)

# Unpack a collection
fruits = ["apple", "banana", "papai"]
q, w, e = fruits
print(q)
print(w)
print(e)

# Output multiple variables
print(q, w, e)
print(q + w + e)
