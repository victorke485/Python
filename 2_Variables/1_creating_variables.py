# Variables are containers for storing data values.
# Variable is created the moment you first assign a value to it.
x = 5
y = "Victor"
print(x)
print(y)

# Casting - specify data type of a variable
a = str("Alex")
b = int(33)
c = float(2)
print(a)
print(b)
print(c)

# Get the type of variable
print(type(a))
print(type(b))
print(type(c))

