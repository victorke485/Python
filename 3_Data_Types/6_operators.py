# Identity Operator - used to compare the objects, not if they are equal, but if they are actually the same object, with the same memory location
x = "python"
y = "java"
print(x is y)
print(x is not y)

# Membership Operators - used to test if a sequence is presented in an object
print("p" in x)
print("j" in x)

