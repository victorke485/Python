fruits = ["apple", "banana", "cherry"]
for x in fruits:
    print(x)

# Looping through a string
for x in "Python":
    print(x)

# The break statement
for x in "Python":
    print(x)
    if x == "y":
        break

# The continue statement
for x in "Hello":
    if x == "o":
        continue
    print(x)

# The range() Function
for x in range(9):
    print(x)

# Else in for loop
for x in range(6):
    print(x)
else:
    print("Finally finished!")
